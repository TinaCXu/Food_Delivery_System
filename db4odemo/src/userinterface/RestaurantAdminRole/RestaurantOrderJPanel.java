    public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        model.setRowCount(0);
        for(WorkRequest wr:restaurant.getWorkQueue().getWorkRequestList()){
            LabTestWorkRequest ltwr = (LabTestWorkRequest) wr;
            Object[] row = new Object[6];
            row[0] = ltwr;
            row[1] = ltwr.getReceiver();
            row[2] = ltwr.getMenu();
            row[3] = ltwr.getStatus();
            row[4] = ltwr.getDeliveryMan() == null ? "no delivery man":ltwr.getDeliveryMan().toString();
            row[5] = ltwr.getSender().getName();
            model.addRow(row);            
        }
    }
