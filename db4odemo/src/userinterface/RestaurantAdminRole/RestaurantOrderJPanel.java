    private void btnCommentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCommentActionPerformed
        // TODO add your handling code here:
        int selectedRow = workRequestJTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select an order to view detail.");
            return;
        }else{
            DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
            LabTestWorkRequest ltwr = (LabTestWorkRequest) model.getValueAt(selectedRow, 0);

            RestaurantOrderDetailJPanel rodjp = new RestaurantOrderDetailJPanel(userProcessContainer, ltwr, restaurant, ecosystem);
            userProcessContainer.add("RestaurantOrderDetailJPanel",rodjp);
            CardLayout crdLyt = (CardLayout) userProcessContainer.getLayout();
            crdLyt.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnCommentActionPerformed

