    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(comboStatus.getSelectedItem() == null){
            JOptionPane.showMessageDialog(this, "Please ensure to select status.");
            return;
        }
        if(comboStatus.getSelectedItem().toString().equals("Accepted") & comboDeliver.getSelectedItem() == null){
            JOptionPane.showMessageDialog(this, "Please select a delivery man.");
            return;
        }
        ltwr.setStatus(comboStatus.getSelectedItem().toString());
        
        if(comboDeliver.getSelectedItem() != null){
            int selectedID = Integer.parseInt(comboDeliver.getSelectedItem().toString().substring(comboDeliver.getSelectedItem().toString().indexOf(":")+1, comboDeliver.getSelectedItem().toString().indexOf(")")));
            Employee selectedE = ecosystem.getEmployeeDirectory().searchEmployee(selectedID, employeeRole);
            DeliveryMan selectedD = ecosystem.getDeliveryManDirectory().searchDeliveryMan(selectedE);        
            
            if( oldDeliver != selectedD){
                ltwr.setDeliveryMan(selectedD);
                selectedD.getRequestList().add(ltwr);
            }
        }
                
        
        JOptionPane.showMessageDialog(this, "Order save successfully.");
        back();
    }//GEN-LAST:event_btnSaveActionPerformed

    public void back(){
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        RestaurantOrderJPanel rojp = (RestaurantOrderJPanel) component;
        rojp.populateRequestTable();
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.last(userProcessContainer);

    }
    
