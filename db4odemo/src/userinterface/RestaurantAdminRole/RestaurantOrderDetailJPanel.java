    public void populateComboDeliver(){
        for(DeliveryMan dm:ecosystem.getDeliveryManDirectory().getDeliveryManList()){
            if(dm.getRestaurant() == restaurant){
                comboDeliver.addItem(dm.getEmployee().toString() + "(ID:"+dm.getEmployee().getId()+")");
                employeeRole = dm.getEmployee().getRole();
            }
        }
    }
    
