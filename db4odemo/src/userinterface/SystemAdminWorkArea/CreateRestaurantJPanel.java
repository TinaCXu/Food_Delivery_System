    public void populateCombo(){
        for(Employee e: ecosystem.getEmployeeDirectory().getEmployeeList()){
            if(e.getRole().getClass() == AdminRole.class){
                AdminRole adminRole = (AdminRole) e.getRole();
                if(adminRole.getRestaurant() == null){
                    jComboBox1.addItem(e.toString() + "(ID:"+e.getId()+")");
                    employeeRole = e.getRole();                    
                }
            }
        }
    }
