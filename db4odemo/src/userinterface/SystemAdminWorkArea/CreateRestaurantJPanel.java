    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(jTextField1.getText().isBlank() || jComboBox1.getSelectedItem() == null){
            JOptionPane.showMessageDialog(this, "Please complete all information before saving.");
            return;
        }
        
        String name = jTextField1.getText();        
        int selectedID = Integer.parseInt(jComboBox1.getSelectedItem().toString().substring(jComboBox1.getSelectedItem().toString().indexOf(":")+1, jComboBox1.getSelectedItem().toString().indexOf(")")));
        Employee selectedE = ecosystem.getEmployeeDirectory().searchEmployee(selectedID, employeeRole);
        
        Restaurant newR = ecosystem.getRestaurantDirectory().addRestaurant();
        newR.setName(name);
        newR.setAdminEmployee(selectedE);
        
        MenuDirectory md = new MenuDirectory();
        md.setRestaurnat(newR);
        newR.setMenuDirectory(md);
        
        
        WorkQueue wq = new WorkQueue();
        wq.setRestaurant(newR);
        newR.setWorkQueue(wq);
        
        AdminRole adminRole = (AdminRole) selectedE.getRole();
        adminRole.setRestaurant(newR);
        
        JOptionPane.showMessageDialog(this, "Create restaurant successfully.");
        back();
    }//GEN-LAST:event_btnSaveActionPerformed

